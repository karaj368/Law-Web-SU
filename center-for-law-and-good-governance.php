            <h2>Overview</h2>
            <div class="space16"></div>
            <p>A Center for Law and Good Governance was inaugurated on 11th April, 2015 by Hon’ble Governor of West
              Bengal, Bihar, Mizoram and Tripura Shri Keshari Nath Tripathi at a function held at School of Law and
              Constitutional Studies, Shobhit University, Meerut. The center is a part of law faculty of Shobhit
              University, Meerut.
              Harvard and Oxford educated Supreme Court lawyer Dr. Surat Singh has been appointed as Chairman of the
              Center.
            </p>
            <p>Introducing the purposes for which the Center for Law and Good Governance has been set up, Dr. Surat
              Singh said that since time immemorial, thinkers-both from East and West have wondered about what is the
              purpose of life and what kind of structures of society will be conducive to achieve the best possible
              goals of life. Thus law and government should always be guided by the highest goals a society and
              individuals can aspire for Law and political structure are made to make it possible for society and
              individual to achieve the highest possible fulfillment and blessedness (Ananda in Sanskrit and Eudemonia
              in Greek). For this purpose, we need a constant examination and innovation of our legal systems. Like
              life, Law is dynamic. What may be relevant today may not be relevant after few decades. It is the law
              which has to adjust to life and not other way round because Laws and structures are made for human beings
              and not human beings for laws.</p>
            <p>Dr. Surat Singh emphasized that we need to bridge the gap between what we need and what we have. The
              center will reflect upon what kind of laws and systems of governance we have at the moment and what kind
              of laws and systems of governance we need and we need to do more to make India a great nation and Indian
              people a good citizenry and world a better place to live.</p>
            <p>To advise the center, a team of top legal luminary both from India as well as from other parts of the
              world have very kindly consisted to be on the advisory council of the center. </p>
            <p><strong>DR. SURAT SINGH, CHAIRMAN CENTRE FOR LAW AND GOOD GOVERNANCE </strong>(Harvard and Oxford
              educated top lawyer of the Supreme Court of India) </p>
