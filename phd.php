            <p>A Ph.D. program is offered in two modes- Full time and Part time and not in a distance-learning mode.
              Hence, coursework, participation in various interactions, submission of progress reports through
              presentations etc. on regular basis is mandatory during the program. A Full-time Ph.D. scholar shall mean
              a candidate devoting full time for completing the requirements of the program and a part- time Ph.D.
              scholar shall mean a candidate who is devoting a part of his time towards the pursuit of the program while
              discharging his official / employment obligations.</p>

            <div class="space24"></div>
            <h2>Program Overview</h2>
            <p>Ph.D. full time is for minimum 3 years. Maximum duration is 6 years. Shobhit University encourages
              interdisciplinary research and exchange of ideas and knowledge in various related fields. Consequently, it
              allows scholars to appoint a Co-Guide from outside Shobhit University as per university rules. The main
              Guide for the scholar during the Ph.D. program will be from Shobhit University, appointed as per the
              research topic and Guide availability.
            </p>

