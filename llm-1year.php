            <h2>Eligibility Criteria and Mode of Lecture Delivery</h2>
            <p>Admission to LL.M. (One Year) Program is open to a candidate who holds a Law degree of 3/5 years duration
              with minimum 50% marks from an UGC recognized university. He/She shall have a relevant experience of
              minimum 3 years after a Law Degree.
              Mode of Lecture Delivery: Due to the COVID -19 Pandemic, the School opted for blended learning and the
              lectures will be delivered online /in-person on Saturday and Sundays.</p>

            <h2>Course-Curriculum</h2>
            <p>We're the first to admit it: Learning the law is not easy. We have to sharpen a range of skills,
              including analysis, writing, and persuasion. We also have to learn a canon of precedents, concepts,
              theories, and procedures. As a strong academic community, committed to our students, we have designed
              curricula that produce broadly educated lawyers. We have developed an approach to teaching the law that is
              both challenging and supportive in compliance with the recommendations and guidelines of the apex bodies.
